"""
Тесты для задачи "Возрастная группа"
"""

def age_group(age):
    """Возвращает возрастную группу."""
    if age <= 13:
        return "детство"
    elif age <= 24:
        return "молодость"
    elif age <= 59:
        return "зрелость"
    else:
        return "старость"

def run_tests():
    tests = [
        # (возраст, ожидаемая_группа)
        (0, "детство"),      # минимальное значение
        (5, "детство"),      # внутри диапазона
        (13, "детство"),     # граница: последний день детства
        (14, "молодость"),   # граница: начало молодости
        (20, "молодость"),   # внутри диапазона
        (24, "молодость"),   # граница: конец молодости
        (25, "зрелость"),    # граница: начало зрелости
        (40, "зрелость"),    # внутри диапазона
        (59, "зрелость"),    # граница: конец зрелости
        (60, "старость"),    # граница: начало старости
        (65, "старость"),    # внутри диапазона
        (100, "старость"),   # большое значение
        (120, "старость"),   # максимальное возможное
    ]
    
    print("=== Тесты для 'Возрастная группа' ===\n")
    all_passed = True
    
    for i, (age, expected) in enumerate(tests, 1):
        result = age_group(age)
        passed = result == expected
        all_passed = all_passed and passed
        status = "✅" if passed else "❌"
        print(f"{status} Тест {i}: возраст = {age}")
        print(f"   Результат: {result}")
        print(f"   Ожидалось: {expected}\n")
    
    if all_passed:
        print("🎉 Все тесты пройдены!")
    else:
        print("❌ Некоторые тесты не пройдены.")

run_tests()
