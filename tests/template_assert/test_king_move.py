"""
Тесты для задачи "Ход короля ♔🌶️"
"""

def solve(x1, y1, x2, y2):
    if abs(x1 - x2) <= 1 and abs(y1 - y2) <= 1:
        return "YES"
    else:
        return "NO"

def test_king_move():
    tests = [
        # По горизонтали
        (1, 1, 2, 1, "YES"),
        (5, 3, 6, 3, "YES"),
        
        # По вертикали
        (3, 2, 3, 3, "YES"),
        (7, 5, 7, 4, "YES"),
        
        # По диагонали
        (2, 2, 3, 3, "YES"),
        (4, 5, 5, 4, "YES"),
        (8, 8, 7, 7, "YES"),
        
        # Та же клетка (разные? условие: различные, но на всякий случай)
        (3, 3, 3, 3, "YES"),
        
        # Не ходит (больше чем на 1 клетку)
        (1, 1, 3, 1, "NO"),
        (1, 1, 1, 3, "NO"),
        (1, 1, 3, 3, "NO"),
        (4, 4, 6, 6, "NO"),
        (2, 5, 4, 7, "NO"),
        
        # Границы доски
        (1, 1, 1, 2, "YES"),   # вертикаль
        (1, 1, 2, 1, "YES"),   # горизонталь
        (1, 1, 2, 2, "YES"),   # диагональ
        (8, 8, 7, 8, "YES"),   # горизонталь
        (8, 8, 8, 7, "YES"),   # вертикаль
        (8, 8, 7, 7, "YES"),   # диагональ
        (1, 8, 2, 7, "YES"),   # диагональ
        (8, 1, 7, 2, "YES"),   # диагональ
    ]
    
    for i, (x1, y1, x2, y2, expected) in enumerate(tests, 1):
        result = solve(x1, y1, x2, y2)
        assert result == expected, f"Тест {i} провален: ({x1},{y1}) -> ({x2},{y2}) -> {result}, ожидалось {expected}"
        print(f"✅ Тест {i} пройден: ({x1},{y1}) -> ({x2},{y2}) -> {result}")
    
    print("\n🎉 Все тесты пройдены!")

if __name__ == "__main__":
    test_king_move()
