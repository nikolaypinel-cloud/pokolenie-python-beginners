"""
Тесты для задачи "Вид треугольника 📐"
"""

def solve(a, b, c):
    if a == b == c:
        return "равносторонний"
    elif a == b or a == c or b == c:
        return "равнобедренный"
    else:
        return "разносторонний"

def test_triangle_type():
    tests = [
        # Равносторонние
        (3, 3, 3, "равносторонний"),
        (5, 5, 5, "равносторонний"),
        (1, 1, 1, "равносторонний"),
        
        # Равнобедренные
        (3, 3, 4, "равнобедренный"),
        (5, 6, 5, "равнобедренный"),
        (7, 8, 8, "равнобедренный"),
        (2, 2, 1, "равнобедренный"),
        
        # Разносторонние
        (3, 4, 5, "разносторонний"),
        (6, 7, 8, "разносторонний"),
        (2, 3, 4, "разносторонний"),
        (10, 11, 12, "разносторонний"),
        
        # Крайние случаи (не треугольники, но классифицируются)
        (1, 1, 2, "равнобедренный"),   # вырожденный
        (1, 2, 3, "разносторонний"),   # вырожденный
        (0, 0, 0, "равносторонний"),   # нулевые стороны
    ]
    
    for i, (a, b, c, expected) in enumerate(tests, 1):
        result = solve(a, b, c)
        assert result == expected, f"Тест {i} провален: ({a}, {b}, {c}) -> {result}, ожидалось {expected}"
        print(f"✅ Тест {i} пройден: ({a}, {b}, {c}) -> {result}")
    
    print("\n🎉 Все тесты пройдены!")

if __name__ == "__main__":
    test_triangle_type()
