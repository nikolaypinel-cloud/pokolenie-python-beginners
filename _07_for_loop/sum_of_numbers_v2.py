"""
Сумма чисел 2
Подсчитать сумму чисел от 1 до n, квадрат которых оканчивается на 2, 5 или 8.
Примечание: математически квадрат числа не может оканчиваться на 2 или 8,
поэтому фактически проверяется только окончание на 5.
"""

def solve(number):
    # Оставляем условие по условию задачи, хотя 2 и 8 математически невозможны
    total = sum(i for i in range(1, number + 1)
                if i**2 % 10 in (2, 5, 8))
    return total

if __name__ == "__main__":
    n = int(input())
    print(solve(n))
