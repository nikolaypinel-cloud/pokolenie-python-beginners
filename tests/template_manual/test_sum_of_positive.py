"""
Тесты для задачи "Только +"
"""

def sum_positive(a, b, c):
    """Возвращает сумму положительных чисел."""
    total = 0
    if a > 0:
        total += a
    if b > 0:
        total += b
    if c > 0:
        total += c
    return total

def run_tests():
    tests = [
        # (a, b, c, ожидаемая_сумма)
        (1, 2, 3, 6),      # все положительные
        (-1, -2, -3, 0),   # все отрицательные → 0
        (0, 0, 0, 0),      # все нули → 0
        (5, -3, 2, 7),     # два положительных, один отрицательный
        (-10, 0, 4, 4),    # один положительный, ноль, отрицательный
        (0, 7, -1, 7),     # ноль, положительный, отрицательный
        (100, -100, 0, 100),  # большое положительное, отрицательное, ноль
        (-1, 0, 1, 1),     # отрицательный, ноль, положительный
        (0, 0, 5, 5),      # два нуля, положительный
        (0, -5, 0, 0),     # ноль, отрицательный, ноль → 0
    ]
    
    print("=== Тесты для 'Только +' ===\n")
    all_passed = True
    
    for i, (a, b, c, expected) in enumerate(tests, 1):
        result = sum_positive(a, b, c)
        passed = result == expected
        all_passed = all_passed and passed
        status = "✅" if passed else "❌"
        print(f"{status} Тест {i}: ({a}, {b}, {c})")
        print(f"   Результат: {result}")
        print(f"   Ожидалось: {expected}\n")
    
    if all_passed:
        print("🎉 Все тесты пройдены!")
    else:
        print("❌ Некоторые тесты не пройдены.")

run_tests()
