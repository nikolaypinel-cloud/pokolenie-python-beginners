"""
Тесты для задачи "Наименьшее из четырёх чисел 🌶️"
"""

def find_min(a, b, c, d):
    """Возвращает наименьшее из четырёх чисел (имитация твоего алгоритма)."""
    # Твой алгоритм в чистом виде (без изменения исходных переменных вне функции)
    nums = [a, b, c, d]
    min_val = nums[0]
    for x in nums[1:]:
        if x < min_val:
            min_val = x
    return min_val

def run_tests():
    tests = [
        # (a, b, c, d, ожидаемый_минимум)
        (1, 2, 3, 4, 1),           # возрастающий порядок
        (4, 3, 2, 1, 1),           # убывающий порядок
        (5, 5, 5, 5, 5),           # все равны
        (-5, -10, -3, -1, -10),    # отрицательные
        (0, 0, -1, 0, -1),         # с нулём и отрицательным
        (100, 200, 50, 150, 50),   # перемешанные
        (-100, -200, -50, -150, -200),  # отрицательные перемешанные
        (10, 10, 5, 10, 5),        # повторяющиеся, минимум один
        (7, 7, 7, 8, 7),           # три одинаковых, один больше
        (-1, -1, -1, -2, -2),      # три одинаковых, один меньше
    ]
    
    print("=== Тесты для 'Наименьшее из четырёх чисел' ===\n")
    all_passed = True
    
    for i, (a, b, c, d, expected) in enumerate(tests, 1):
        result = find_min(a, b, c, d)
        passed = result == expected
        all_passed = all_passed and passed
        status = "✅" if passed else "❌"
        print(f"{status} Тест {i}: ({a}, {b}, {c}, {d})")
        print(f"   Результат: {result}")
        print(f"   Ожидалось: {expected}\n")
    
    if all_passed:
        print("🎉 Все тесты пройдены!")
    else:
        print("❌ Некоторые тесты не пройдены.")

run_tests()
