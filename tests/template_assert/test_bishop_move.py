"""
Тесты для задачи "Ход слона ♗🌶️"
"""

def solve(x1, y1, x2, y2):
    return "YES" if abs(x1 - x2) == abs(y1 - y2) else "NO"

def test_bishop_move():
    tests = [
        # Диагонали
        (1, 1, 8, 8, "YES"),
        (8, 8, 1, 1, "YES"),
        (4, 4, 6, 6, "YES"),
        (2, 3, 5, 6, "YES"),   # разность 3
        (1, 8, 8, 1, "YES"),
        (7, 2, 4, 5, "YES"),   # разность 3
        
        # Не диагонали
        (1, 1, 1, 2, "NO"),
        (1, 1, 2, 1, "NO"),
        (1, 1, 2, 3, "NO"),
        (5, 5, 6, 8, "NO"),
        (8, 8, 8, 7, "NO"),
        
        # Та же клетка (разные? но на всякий случай)
        (3, 3, 3, 3, "YES"),   # разность 0 == 0
        
        # Границы доски
        (1, 2, 8, 9, "NO"),   # 9 вне доски, но формула всё равно работает
        (8, 1, 1, 8, "YES"),
        (8, 1, 1, 7, "NO"),
    ]
    
    for i, (x1, y1, x2, y2, expected) in enumerate(tests, 1):
        result = solve(x1, y1, x2, y2)
        assert result == expected, f"Тест {i} провален: ({x1},{y1}) -> ({x2},{y2}) -> {result}, ожидалось {expected}"
        print(f"✅ Тест {i} пройден: ({x1},{y1}) -> ({x2},{y2}) -> {result}")
    
    print("\n🎉 Все тесты пройдены!")

if __name__ == "__main__":
    test_bishop_move()
