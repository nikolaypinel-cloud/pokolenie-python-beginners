"""
Тесты для задачи "Красивое число 🌸🌶️"
"""

def solve(n):
    if 1000 <= n <= 9999 and (n % 7 == 0 or n % 17 == 0):
        return "YES"
    else:
        return "NO"

def test_beautiful_number():
    tests = [
        # Четырёхзначные, красивые
        (1001, "YES"),   # 1001 % 7 == 0
        (1003, "YES"),   # 1003 % 17 == 0
        (1008, "YES"),   # 1008 % 7 == 0
        (1010, "NO"),    # четырёхзначное, но не делится
        (9996, "YES"),   # 9996 % 7 == 0
        (9999, "NO"),    # четырёхзначное, не делится
        
        # Границы четырёхзначных
        (999, "NO"),     # трёхзначное
        (1000, "NO"),    # четырёхзначное, но не делится (1000 % 7 = 6, % 17 = 14)
        (10000, "NO"),   # пятизначное
        
        # Отрицательные (не натуральные, но на всякий случай)
        (-1001, "NO"),
    ]
    
    for i, (n, expected) in enumerate(tests, 1):
        result = solve(n)
        assert result == expected, f"Тест {i} провален: n={n} -> {result}, ожидалось {expected}"
        print(f"✅ Тест {i} пройден: n={n} -> {result}")
    
    print("\n🎉 Все тесты пройдены!")

if __name__ == "__main__":
    test_beautiful_number()
