"""
Тесты для задачи "Интересное число 🤔"
"""

def solve(num):
    n1 = num // 100
    n2 = num // 10 % 10
    n3 = num % 10
    
    # Сортировка по убыванию
    if n1 < n2:
        n1, n2 = n2, n1
    if n1 < n3:
        n1, n3 = n3, n1
    if n2 < n3:
        n2, n3 = n3, n2
    
    return "Число интересное" if n1 - n3 == n2 else "Число неинтересное"

def test_interesting_number():
    tests = [
        (123, "Число неинтересное"),  # 3-1=2, средняя 2 → 2==2? Да → интересное? 3-1=2, средняя 2 → ДА
        (132, "Число неинтересное"),
        (213, "Число неинтересное"),
        (231, "Число неинтересное"),
        (312, "Число неинтересное"),
        (321, "Число неинтересное"),  # 3-1=2, средняя 2 → ДА
        (135, "Число интересное"),    # 5-1=4, средняя 3 → 4≠3 → НЕТ
        (147, "Число неинтересное"),
        (159, "Число неинтересное"),
        (248, "Число интересное"),    # 8-2=6, средняя 4 → 6≠4 → НЕТ
        (369, "Число неинтересное"),
        (420, "Число интересное"),    # 4-0=4, средняя 2 → 4≠2 → НЕТ
        (555, "Число интересное"),    # 5-5=0, средняя 5 → 0≠5 → НЕТ
        (112, "Число неинтересное"),  # 2-1=1, средняя 1 → 1==1 → ДА
        (121, "Число неинтересное"),
        (211, "Число неинтересное"),
        (100, "Число интересное"),    # 1-0=1, средняя 0 → 1≠0 → НЕТ
        (102, "Число неинтересное"),  # 2-0=2, средняя 1 → 2≠1 → НЕТ
        (120, "Число неинтересное"),
        (201, "Число неинтересное"),
        (210, "Число неинтересное"),
    ]
    
    for i, (num, expected) in enumerate(tests, 1):
        result = solve(num)
        assert result == expected, f"Тест {i} провален: num={num} -> {result}, ожидалось {expected}"
        print(f"✅ Тест {i} пройден: num={num} -> {result}")
    
    print("\n🎉 Все тесты пройдены!")

if __name__ == "__main__":
    test_interesting_number()
