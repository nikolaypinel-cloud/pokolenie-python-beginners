"""
Тесты для задачи "Ход ладьи"
"""

def solve(x1, y1, x2, y2):
    if x1 == x2 or y1 == y2:
        return "YES"
    else:
        return "NO"

def test_rook_move():
    tests = [
        # По горизонтали
        (1, 1, 5, 1, "YES"),
        (2, 3, 8, 3, "YES"),
        (4, 4, 4, 4, "YES"),   # та же клетка (разные? условие: различные, но на всякий случай)
        
        # По вертикали
        (3, 2, 3, 7, "YES"),
        (5, 5, 5, 1, "YES"),
        
        # Не ходит
        (1, 1, 2, 2, "NO"),
        (3, 4, 5, 6, "NO"),
        (2, 5, 4, 7, "NO"),
        (1, 8, 8, 1, "NO"),
        
        # Границы доски
        (1, 1, 1, 8, "YES"),   # по вертикали
        (1, 1, 8, 1, "YES"),   # по горизонтали
        (8, 8, 1, 8, "YES"),   # по горизонтали
        (8, 8, 8, 1, "YES"),   # по вертикали
    ]
    
    for i, (x1, y1, x2, y2, expected) in enumerate(tests, 1):
        result = solve(x1, y1, x2, y2)
        assert result == expected, f"Тест {i} провален: ({x1},{y1}) -> ({x2},{y2}) -> {result}, ожидалось {expected}"
        print(f"✅ Тест {i} пройден: ({x1},{y1}) -> ({x2},{y2}) -> {result}")
    
    print("\n🎉 Все тесты пройдены!")

if __name__ == "__main__":
    test_rook_move()
